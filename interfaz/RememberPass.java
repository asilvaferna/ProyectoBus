    public void send() {
        OperacionesBD consulta = new OperacionesBD();
        try {
            String username = jTCorreoELectronico.getText();
            userID = consulta.getUserID(username);
            String usernameB = consulta.getUserUsername(userID);
              if (username.isEmpty()) {
                jlAlert2.setText("Usuario incorrecto.");
            } else if (username.equals(usernameB)) {
                SMS.telefono = consulta.getTelefono(userID);
                SMS.password = consulta.getUserPassword(userID);
                SMS.envioSMS();
                                      new Login().setVisible(true);

                this.setVisible(false);
                
            } else {
                jlAlert2.setText("Usuario incorrecto.");
            }

        } catch (Exception ex) {
            jlAlert2.setText("Usuario incorrecto.");
        }
  
    }

